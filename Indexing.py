# Indexing In Python 
# In Indexing we use Square brackets "[]" to Print Index
# Indexing Is Start From Zero
# Ex. In "Hello" Indexing is h=0,e=1,l=2,l=3,o=4

Index = "Hello"
print(Index[0])
print(Index[1])
print(Index[2])
print(Index[3])
print(Index[4])

# Slicing Of String

print(Index[0:1])
print(Index[0:2])
print(Index[0:3])
print(Index[0:4])
print(Index[0:5])